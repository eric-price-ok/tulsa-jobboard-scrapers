def _map_job_to_function(cursor, job_title: str, location_name: str) -> Optional[int]:
    match_text = f"{job_title or ''} {location_name or ''}".lower()
    for function_name, keywords in _FUNCTION_KEYWORDS.items():
        for keyword in keywords:
            if keyword in match_text:
                cursor.execute("SELECT id FROM functions WHERE name = %s", (function_name,))
                result = cursor.fetchone()
                if result:
                    logger.info(f"  Mapped '{job_title}' to function: {function_name}")
                    return result['id']
    cursor.execute("SELECT id FROM functions WHERE name = %s", ('Other',))
    result = cursor.fetchone()
    if result:
        logger.info(f"  Mapped '{job_title}' to function: Other (no match)")
        return result['id']
    return None


def _parse_city_from_address(address: str, state_code: str) -> Optional[str]:
    """Extract the city from a Fountain location_address string.

    Observed formats:
      "2710 E 11th St, Tulsa, OK 74104, USA"
      "8934 East Latimer Street, Tulsa, OK, 74115, US"
      "Tulsa, OK, US"
    In every case the city is the comma-segment immediately before the one
    starting with the state code.
    """
    if not address or not state_code:
        return None
    parts = [p.strip() for p in address.split(',')]
    for i, part in enumerate(parts):
        if i > 0 and part.upper().startswith(state_code.upper()):
            return parts[i - 1]
    return None


def _street_address(location_address: str) -> Optional[str]:
    """Keep only the street portion of a Fountain location_address string —
    everything after the first comma is city/state/zip, not part of the
    street address (e.g. "3027 S New Haven Ave, Tulsa, OK 74114, USA" ->
    "3027 S New Haven Ave")."""
    if not location_address:
        return None
    street = location_address.split(',')[0].strip()
    return street or None


def _get_or_create_site_by_address(cursor, company_id: int, location_name: str, address1: str,
                                    city_id: Optional[int], state_id: Optional[int],
                                    country_id: Optional[int], logger) -> Optional[int]:
    """Get or create a companysite matched by address1 rather than site_name.

    Fountain's location_name is a department (e.g. "Finance & Payroll",
    "Accounting"), not a site identifier — multiple departments commonly
    share one physical address, and should resolve to the same companysite
    row rather than creating a duplicate per department. This intentionally
    bypasses utils.company_operations.get_or_create_company_site(), which
    matches by site_name and would create a separate row per department.
    """
    if not address1:
        return None

    cursor.execute(
        "SELECT id FROM companysite WHERE company_id = %s AND LOWER(address1) = LOWER(%s)",
        (company_id, address1)
    )
    result = cursor.fetchone()
    if result:
        logger.info(f"  Found existing company site at '{address1}' (ID: {result['id']})")
        return result['id']

    cursor.execute("SELECT id FROM companysitetype WHERE name = %s", ('Branch Office',))
    type_result = cursor.fetchone()
    site_type_id = type_result['id'] if type_result else None

    cursor.execute("""
        INSERT INTO companysite (company_id, site_name, city_id, address1, state_id, country_id, site_type, is_active)
        VALUES (%s, %s, %s, %s, %s, %s, %s, %s)
        RETURNING id
    """, (company_id, location_name.strip() if location_name else None, city_id, address1,
          state_id, country_id, site_type_id, True))
    result = cursor.fetchone()
    site_id = result['id']
    logger.info(f"  Created new company site '{location_name}' at '{address1}' (ID: {site_id})")
    return site_id


_GRADE_SALARY_PATTERN = re.compile(r'Grade:?\s*\S+\s*\(([^)]+)\)', re.IGNORECASE)
_LAST_REVISED_PATTERN = re.compile(
    r'Last Revised Date:?\s*([A-Za-z]+\s+\d{1,2},?\s*\d{4}|[A-Za-z]+\s+\d{4})',
    re.IGNORECASE,
)


def _parse_salary_from_grade(text: str):
    """Parse a line like "Grade: H-5 ($12.37 - $15.69)" into
    (minimum_salary, maximum_salary, pay_frequency). Returns (None, None, None)
    if no Grade line is found. These district pay-grade ranges are hourly
    rates by convention (the "H-" grade prefix), but the source text never
    says "hourly" explicitly, so pay_frequency is asserted rather than
    detected from the text itself.
    """
    match = _GRADE_SALARY_PATTERN.search(text or '')
    if not match:
        return None, None, None
    parsed = parse_salary_text(match.group(1))
    if parsed['minimum_salary'] is None:
        return None, None, None
    return parsed['minimum_salary'], parsed['maximum_salary'], 'hourly'


def _parse_last_revised_date(text: str) -> Optional[datetime]:
    """Parse "Last Revised Date: June 18, 2025" (day+month+year) or
    "Last Revised Date: June 2025" (month+year only — assumes the 1st)."""
    match = _LAST_REVISED_PATTERN.search(text or '')
    if not match:
        return None
    date_str = match.group(1).strip()
    for fmt in ('%B %d, %Y', '%B %d %Y'):
        try:
            return datetime.strptime(date_str, fmt)
        except ValueError:
            continue
    try:
        return datetime.strptime(date_str, '%B %Y')  # month+year only -> day 1
    except ValueError:
        logger.warning(f"  Could not parse Last Revised Date: '{date_str}'")
        return None


def _find_full_description_link(container: Tag) -> Optional[str]:
    """Find the <a> tag immediately following the phrase "Full Job
    Description:" within a rendered description container."""
    found_phrase = False
    for node in container.descendants:
        if isinstance(node, NavigableString):
            if 'full job description' in node.lower():
                found_phrase = True
        elif isinstance(node, Tag) and node.name == 'a' and found_phrase:
            href = node.get('href')
            if href:
                return href
    return None


def _extract_docx_text(file_path: str) -> str:
    try:
        with open(file_path, 'rb') as docx_file:
            result = mammoth.extract_raw_text(docx_file)
            return result.value
    except Exception as e:
        logger.warning(f"  Failed to extract DOCX text: {e}")
        return ""


def _extract_pdf_text(file_path: str) -> str:
    try:
        text = ""
        with open(file_path, 'rb') as pdf_file:
            pdf_reader = PyPDF2.PdfReader(pdf_file)
            for page in pdf_reader.pages:
                text += page.extract_text() + "\n"
        return text
    except Exception as e:
        logger.warning(f"  Failed to extract PDF text: {e}")
        return ""


def _sniff_file_kind(content: bytes) -> Optional[str]:
    """Identify a downloaded Drive file by magic bytes rather than trusting
    Content-Type, which Drive's uc?export=download endpoint often reports
    generically (e.g. application/octet-stream) for both PDFs and DOCX."""
    if content[:5] == b'%PDF-':
        return 'pdf'
    if content[:2] == b'PK':  # DOCX/XLSX/etc. are zip containers
        return 'docx'
    return None


def resolve_external_description(session: requests.Session, url: str) -> Optional[str]:
    """Resolve the "Full Job Description:" link to its text content.

    Handles a native Google Doc (exported as plain text) or a Google
    Drive-hosted file (downloaded and run through mammoth/PyPDF2 depending on
    content type, same as applitrack's DocumentProcessor). Returns None on
    any failure — most commonly because the doc/file isn't shared publicly
    ("anyone with the link can view"), which the caller should treat as
    "fall back to the inline rendered text" rather than a hard error.
    """
    try:
        doc_match = re.search(r'/document/d/([a-zA-Z0-9_-]+)', url)
        if doc_match:
            export_url = f"https://docs.google.com/document/d/{doc_match.group(1)}/export?format=txt"
            response = session.get(export_url, timeout=20)
            response.raise_for_status()
            text = response.text.strip()
            if text and len(text) > 100 and '<html' not in text.lower()[:200]:
                logger.info(f"  Extracted {len(text)} characters from linked Google Doc")
                return text
            logger.warning("  Google Doc export returned no usable text (likely not shared publicly)")
            return None

        file_match = re.search(r'/file/d/([a-zA-Z0-9_-]+)', url) or re.search(r'[?&]id=([a-zA-Z0-9_-]+)', url)
        if file_match:
            download_url = f"https://drive.google.com/uc?export=download&id={file_match.group(1)}"
            response = session.get(download_url, timeout=30)
            response.raise_for_status()
            content_type = response.headers.get('Content-Type', '').lower()
            # Drive's download endpoint often reports a generic Content-Type
            # for both PDFs and DOCX, so sniff the actual file signature
            # first rather than guessing an extraction order and logging a
            # spurious "Failed to extract" warning when the guess is wrong.
            file_kind = _sniff_file_kind(response.content)

            with tempfile.NamedTemporaryFile(delete=False) as temp_file:
                temp_file.write(response.content)
                temp_path = temp_file.name
            try:
                if file_kind == 'pdf' or 'pdf' in content_type:
                    text = _extract_pdf_text(temp_path)
                elif file_kind == 'docx' or 'word' in content_type or 'officedocument' in content_type:
                    text = _extract_docx_text(temp_path)
                elif 'text/plain' in content_type:
                    text = response.text
                else:
                    logger.warning(f"  Could not identify Drive file type (content-type: '{content_type}'), "
                                    f"trying PDF then DOCX extraction")
                    text = _extract_pdf_text(temp_path) or _extract_docx_text(temp_path)
            finally:
                try:
                    os.unlink(temp_path)
                except Exception:
                    pass

            text = (text or '').strip()
            if len(text) > 100:
                logger.info(f"  Extracted {len(text)} characters from linked Drive file")
                return text
            logger.warning("  Drive file yielded no usable text (likely not public, or an unrecognized format)")
            return None

    except Exception as e:
        logger.warning(f"  Could not resolve external description link '{url}': {e}")
        return None

    logger.warning(f"  Unrecognized Google Docs/Drive URL shape: {url}")
    return None


