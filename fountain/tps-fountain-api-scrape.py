#!/usr/bin/env python3
"""
tps-fountain-api-scrape.py
Tulsa Public Schools Fountain scraper (Gen 2)

Fountain (web.fountain.com) is a hiring platform. The public careers page
(https://web.fountain.com/c/tulsa-schools/tulsa-schools/US) is a client-side
rendered SPA — the raw HTML is just a JS bootstrap shell with a
"please enable JavaScript" fallback message, so it can never be parsed
directly with requests + BeautifulSoup.

Instead, the page's own JS calls a public, unauthenticated JSON endpoint to
populate the listing:

    https://web.fountain.com/c/tulsa-schools/tulsa-schools/US.json
    https://web.fountain.com/c/tulsa-schools/tulsa-schools/US.json?page=2
    ...

Confirmed working with a plain HTTP GET — no session/cookies/auth token
required. Each entry gives: title, id (uuid), to_param (URL slug),
location_name (this is a DEPARTMENT name, e.g. "Transportation", NOT a
city — see FRAGILITY NOTE below), location_address (street address string),
location_state_code, job_type (observed null on every sampled record).

FRAGILITY NOTES (flagged per request):
  1. This JSON endpoint is not documented/versioned by Fountain anywhere
     public — it's the same internal endpoint the SPA's JS calls to hydrate
     itself. It could change shape or path on a future Fountain deploy with
     no notice. Treat failures here as "check if the endpoint moved" first.
  2. The Fountain JSON has no posted-date field at all. Instead, date_posted
     is pulled from "Last Revised Date: <Month Day, Year>" inside the linked
     Google Doc/Drive job description document, when present. Not every
     posting has one — for those, date_posted defaults to today's date rather
     than an actual posted date, so it should not be read as reliable
     "recently posted" signal for every listing.
  3. The list endpoint has no job description field. The per-opening detail
     page (https://web.fountain.com/c/tulsa-schools/tulsa-schools/US/<to_param>)
     is ALSO a bare JS shell with no server-rendered fallback (confirmed via
     direct fetch), so getting anything at all requires Selenium to render
     it. The rendered page only has a short blurb + a "Full Job Description:"
     link out to an externally-hosted Google Doc/Drive file (usually a PDF)
     with the real content — that linked doc, not the Fountain page itself,
     is the authoritative description AND is where salary/date come from
     (see resolve_external_description() and the Grade/Last Revised Date
     parsing below). Selenium is only used to render the Fountain page long
     enough to find that link — the actual content extraction happens
     against the linked doc/PDF. The link is searched for anywhere in the
     rendered body (not scoped to a specific container), since the "jss###"
     classname the summary text lives in is auto-generated by react-jss/
     Material-UI based on stylesheet insertion order and is NOT guaranteed
     stable across a future Fountain deploy; that specific div.jss222.jss223
     container, then generic extraction (main/article/[role=main]/body),
     are only used as a last-resort fallback if the link itself can't be
     found or resolved. If the linked doc isn't publicly shared ("anyone
     with the link can view"), the export/download fails gracefully and
     falls back to whatever inline text is available — but then salary and
     date parsing have nothing to work with either, since "Grade:"/"Last
     Revised Date:" only appear in the linked doc, never in the Fountain
     page itself.

     If BOTH the link search and every fallback come up empty on every
     single job (not just some), the detail page likely isn't hydrating any
     real content at all in headless Selenium — the detail page's raw HTML
     includes a Cloudflare challenge-platform script
     (/cdn-cgi/challenge-platform/scripts/jsd/main.js), and Cloudflare's bot
     scoring often flags a datacenter IP (like a production server) much
     more aggressively than a residential browser session. get_job_details()
     now polls for the rendered body text to actually grow (rather than a
     fixed sleep) and logs explicitly if the page looks like a Cloudflare
     interstitial rather than real content, so a "Page appears to be a
     Cloudflare challenge/interstitial" warning in the logs confirms this is
     the cause. If that's confirmed and persists, the practical options are:
     a residential/rotating proxy, a stealth-patched browser driver
     (undetected-chromedriver or similar), or asking TPS/Fountain support
     whether an API key/allowlisting is available — plain Selenium is
     unlikely to get past active bot-management on its own.
  4. No rate limiting was hit while probing this endpoint manually, but it
     was only requested a handful of times. A scheduled scrape should keep
     the existing polite delay between requests (see time.sleep below) and
     back off / alert on repeated 429s rather than retrying aggressively.

location_name is actually the hiring department (e.g. "Transportation",
"Child Nutrition", "Multiple School Sites") rather than a city, so it cannot
be matched against the served-cities table directly. Multiple departments
routinely share one physical building (e.g. "Finance & Payroll" and
"Accounting" both sit at "3027 S New Haven Ave, Tulsa, OK 74114, USA"), so
company sites are matched by address1 (the street-only portion of
location_address — see _street_address()) rather than by department name.
This intentionally bypasses utils.company_operations.get_or_create_company_site(),
which matches by site_name and would otherwise create a separate site per
department at the same address. If no site exists yet at that address, one
is created as a Branch Office. The job's own city_id is resolved separately
by parsing the city out of the full location_address (TPS's whole feed is
Tulsa-area, so this is expected to resolve to Tulsa or a nearby served city
on virtually every record).
"""

